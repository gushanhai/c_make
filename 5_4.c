//练习5：输入三角形的三边长，判断能否构成三角形
//如果能，输出是什么三角形（等边、等腰、直角、普通）。如果不能，输出不能

#include<stdio.h>
int main()
{
	//1边
	int a = 0;
	//2边
	int b = 0;
	//3边
	int c = 0;
	scanf("%d %d %d", &a, &b, &c);
	if (a + b > c || a + c > b || c + b > a)
	{
		if (a == b|| b == c || a == c)
		{
			if (a == c||b == c)
			{
				printf("是等边三角形");
			}
			else
			{
				printf("是等腰三角形");
			}
		}
		else if (a * a + b * b == c * c || a * a + c * c == b * b || b * b + c + c == a + a)
		{
			printf("是直角三角形");
		}
		else
		{
			printf("普通三角形");
		}

	}
	else
	{
		printf("不是三角形");
	}
	return 0;
}
